router.post('/', (req, res, next) => {
    let myAuth = new error.KeyAuthentifictaion(req.query.key);
    if(!myAuth.authentifictaion()) return res.send(new error.BadRequestError('Bad API Key'));
    
    try{
        req.body = JSON.parse(Object.keys(req.body)[0]);
    } catch(err) {
        req.body = req.body;
    }
    let slot = {
        idSlot: uuid(),
        name: req.body.data.name,
        order: req.body.data.order,
        category: req.body.data.category,
        idUniverse: req.body.data.idUniverse,
        deleted: null
    };

    slotFunction.createOne(slot)
        .then((result) => {
            return res.send(result);
        })
        .catch((err) => {
            return res.send(new error.NotFoundError(err));
        });
});

router.put('/:id', async (req, res, next) => {
    let myAuth = new error.KeyAuthentifictaion(req.query.key);
    if(!myAuth.authentifictaion()) return res.send(new error.BadRequestError('Bad API Key'));
    
    let id = req.params.id;

    try{
        req.body = JSON.parse(Object.keys(req.body)[0])
    } catch(err) {
        req.body = req.body
    }
    let date = null;
    if(req.body.data.deleted){
        date = new Date().toJSON().slice(0, 10);
    }
    let slot = {
        name: req.body.data.name,
        order: req.body.data.order,
        category: req.body.data.category,
        deleted: date || null
    };

    await slotFunction.getOneById(id)
        .then((result) => {
            if(!slot.name) slot.name = result.name;
            if(!slot.order) slot.order = result.order;
            if(!slot.category) slot.category = result.category;
            if(!slot.deleted) slot.deleted = result.deleted;
        })
        .catch((err) => {
            return res.send(new error.NotFoundError(err));
        });

    slotFunction.updateOne(id, slot)
        .then((result) => {
            return res.send(result);
        })
        .catch((err) => {
            return res.send(new error.NotFoundError(err));
        });
});

