router.post('/', (req, res, next) => {
    let myAuth = new error.KeyAuthentifictaion(req.query.key);
    if(!myAuth.authentifictaion()) return res.send(new error.BadRequestError('Bad API Key'));
    
    try{
        req.body = JSON.parse(Object.keys(req.body)[0]);
    } catch(err) {
        req.body = req.body;
    }
    let nation = {
        idNation: uuid(),
        name: req.body.data.name,
        libelle: req.body.data.libelle,
        idUniverse: req.body.data.idUniverse,
        deleted: null
    };

    nationFunction.createOne(nation)
        .then((result) => {
            return res.send(result);
        })
        .catch((err) => {
            return res.send(new error.NotFoundError(err));
        });
});

router.put('/:id', async (req, res, next) => {
    let myAuth = new error.KeyAuthentifictaion(req.query.key);
    if(!myAuth.authentifictaion()) return res.send(new error.BadRequestError('Bad API Key'));
    
    let id = req.params.id;

    try{
        req.body = JSON.parse(Object.keys(req.body)[0])
    } catch(err) {
        req.body = req.body
    }
    let date = null;
    if(req.body.data.deleted){
        date = new Date().toJSON().slice(0, 10);
    }
    let nation = {
        name: req.body.data.name,
        libelle: req.body.data.libelle,
        deleted: date || null
    };

    await nationFunction.getOneById(id)
        .then((result) => {
            if(!nation.name) nation.name = result.name;
            if(!nation.libelle) nation.libelle = result.libelle;
            if(!nation.deleted) nation.deleted = result.deleted;
        })
        .catch((err) => {
            return res.send(new error.NotFoundError(err));
        });

    nationFunction.updateOne(id, nation)
        .then((result) => {
            return res.send(result);
        })
        .catch((err) => {
            return res.send(new error.NotFoundError(err));
        });
});

